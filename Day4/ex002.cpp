// 사용자로부터 입력받은 2개의 정수의 합을 계산하여 출력
// Windows 사용자는 scanf, strcpy 등 함수 사용 시 경고가 발생할 수 있으므로
// 아래 매크로를 추가하면 경고를 비활성화할 수 있음
#define _CRT_SECURE_NO_WARNINGS  

// 표준 입출력 함수를 사용하기 위한 헤더파일
// printf(), scanf() 함수를 사용하기 위해 반드시 필요
#include <stdio.h>  

// 프로그램의 시작점인 main 함수
int main(void)
{
    // 정수를 저장하기 위한 변수 3개 선언
    // x: 첫 번째 입력값
    // y: 두 번째 입력값
    // sum: 두 수의 합을 저장할 변수
    int x;
    int y;
    int sum;

    // 사용자에게 첫 번째 정수를 입력하라는 안내문 출력
    printf("첫번째 숫자를 입력하세요: ");
    // scanf 함수로 정수를 입력받아 변수 x에 저장
    // %d : 정수 입력 형식 지정자
    // &x : 변수 x의 메모리 주소를 전달 (scanf는 주소를 알아야 값을 저장 가능)
    scanf("%d", &x);

    // 사용자에게 두 번째 정수를 입력하라는 안내문 출력
    printf("두번째 숫자를 입력하세요: ");
    // 두 번째 정수를 입력받아 변수 y에 저장
    scanf("%d", &y);

    // x와 y의 합을 계산하여 sum에 저장
    sum = x + y;

    // 계산된 결과(sum)를 출력
    // %d : 정수 출력 형식 지정자
    printf("두수의 합: %d", sum);

    // 프로그램이 정상적으로 종료되었음을 운영체제에 알림
    return 0;
}
